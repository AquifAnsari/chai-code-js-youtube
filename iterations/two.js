// while loop

let index = 0
while (index < 10) {
    console.log(`index is ${index}`);
    index = index + 2
}


let myArr = ["ironman", "batman", "superman"]

let arr = 0

while (arr < myArr.length) {
    console.log(`value is ${arr}`);
    arr = arr + 1

}

